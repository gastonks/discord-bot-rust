struct Handler;

#[async_trait]
impl EventHandler for Handler {
    async fn message (&self, ctx: Context, msg: Message) {

        //println!("{:?}", msg.content);
        //println!("{:?}", &msg.content[0..10]);

        match msg.content.as_str() {
            MESSAGE_COMMAND => if let Err(why) = msg.channel_id.say(&ctx.http, MESSAGE).await {
                println!("Error sending message: {:?}", why);
            },
            GUS_COMMAND => if let Err(why) = msg.channel_id.say(&ctx.http, GUS_MESSAGE).await {
                println!("Error sending message: {:?}", why);
            },
            GRUH_COMMAND => if let Err(why) = msg.channel_id.say(&ctx.http, GRUH_MESSAGE).await {
                println!("Error sending message: {:?}", why);
            },
            ROLL_COMMAND => {
                let roll = rand::thread_rng().gen_range(1..101);

                let mut msg_roll = String::from("You rolled ");
                msg_roll.push_str(&roll.to_string());

                if let Err(why) = msg.channel_id.say(&ctx.http, msg_roll).await {
                    println!("Error sending message: {:?}", why);
                }

            },
            _ => ()
        }

        if &msg.content[0..5] == GROUL_COMMAND {

            let nbr_bullet: u8 = msg.content[6..7].parse::<u8>().unwrap();

            println!("{}", &msg.content[8..]);

            //println!("{}", nbr_bullet);
            if nbr_bullet >= 1 && nbr_bullet <= 6 {
                for x in 0..nbr_bullet{
                    let roll = rand::thread_rng().gen_range(1..7);
                    println!("{}", roll);

                    if roll == 1 {
                        if let Err(why) = msg.channel_id.say(&ctx.http, "**BANG!**").await {
                            println!("Error sending message: {:?}", why);
                        }
                        break;
                    } else {
                        if let Err(why) = msg.channel_id.say(&ctx.http, "*You survived...*").await {
                            println!("Error sending message: {:?}", why);
                        }
                    }
                }
            } else{
                if let Err(why) = msg.channel_id.say(&ctx.http, "Bien tenter, mais non.").await {
                    println!("Error sending message: {:?}", why);
                }
            }
        }
    }

    async fn ready(&self, _: Context, ready: Ready) {
        println!("{} is connected.", ready.user.name);
    }
}


